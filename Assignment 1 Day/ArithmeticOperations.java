public class ArithmeticOperations {
    public static void main(String[] args) {
        
        int result1 = -5 + 8 * 6; // a
        int result2 = (55 + 9) % 9; // b
        int result3 = 20 + -3 * 5 / 8; // c
        int result4 = 5 + 15 / 3 * 2 - 8 % 3; // d

        
        System.out.println("Result of -5 + 8 * 6: " + result1);
        System.out.println("Result of (55 + 9) % 9: " + result2);
        System.out.println("Result of 20 + -3 * 5 / 8: " + result3);
        System.out.println("Result of 5 + 15 / 3 * 2 - 8 % 3: " + result4);
    }
}